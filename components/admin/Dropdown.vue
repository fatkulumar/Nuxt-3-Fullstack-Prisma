  <script setup lang="ts">
  import { initFlowbite } from 'flowbite'
export type NxLink = {
  name: string;
  link?: string;
  disabled?: boolean;
  show?: boolean;
  hasBorder?: boolean;
  showChildren?: boolean;
  children?: Links;
  bold?: boolean;
  group?: string;
};

const props = defineProps({
  menu: Array<NxLink>,
});

const profileStore = useProfileStore();
const { logout } = useAuth();
const isLoggedIn = ref(false);
const showMenu = ref(false);

/**
 * Sets showMenu to opposite of given value
 * @param value Boolean value
 */
function toggleShowMenu(value: boolean) {
  showMenu.value = !value;
}

/**
 * @desc Receives clicked menu item and emits it
 * @param menuItem Clicked menu item from menu
 */
function menuClicked(menuItem: NxLink, menuLink: NxLink) {
  if (menuItem.toLowerCase() === "logout") logMeOut();
  else if (menuItem.toLowerCase() === "dashboard") navigateTo("/admin");
  else navigateTo(menuLink);
  showMenu.value = false;
}

async function logMeOut() {
  const { status } = await logout();
  if (status === "success") {
    // Clear store variables
    isLoggedIn.value = false;
    profileStore.clearProfile();
    profileStore.setIsLoggedIn(false);
    navigateTo("/login");
  }
}

onMounted(() => {
    initFlowbite();
})

</script>
  
